    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed

        if(isLoginDetailsValid()){
            connect();
            PreparedStatement selectUser;
            String query = "SELECT UserID FROM Users WHERE Username = ? AND "
                    + "Password = ? AND UserLevelName = ?";
            try {
                
                selectUser = connection.prepareStatement(query);
                selectUser.setString(1, username);
                selectUser.setString(2, password);
                selectUser.setString(3, userLevelName);
                
                resultSet = selectUser.executeQuery();
                resultSet.last();
                
                if(resultSet.getRow() == 1){
                    new HomePage(userLevelName).setVisible(true);
                    this.setVisible(false);
                }else{
                     JOptionPane.showMessageDialog(this,"Wrong username or "
                             + "password, or possible your user level.! ","Login Page",JOptionPane.WARNING_MESSAGE);
                }
                
            } catch (SQLException sqlException) {
                JOptionPane.showMessageDialog(this,"Error: " + sqlException.getMessage());
            }finally{
                close();
            }
        }
    }//GEN-LAST:event_btLoginActionPerformed

