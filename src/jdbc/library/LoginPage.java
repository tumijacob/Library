    /*
     * Validating username and password
     * @return true if username and passwoed are valid or are not left blank
     */
    public boolean isLoginDetailsValid(){
        
        username = yxtUsername.getText();
        password = encrypPassword(new String(txtPassword.getPassword()));
        userLevelName = cbUserlevel.getSelectedItem().toString();
        
        boolean valid = false;
        if(username.length() == 0 || password.length() == 0){
            JOptionPane.showMessageDialog(this,"Please enter both username and "
                    + "password.","Error", JOptionPane.ERROR_MESSAGE);
        }else{
            valid = true;
        }
        return valid;
    }
    
    /**
     * Encrypt password 
     * @param password password of the user
     * @return new encrypted password
     */
    public static String encrypPassword(String password){
       
        String newPassword = "";
        for(int i = 0; i < password.length(); i++){
            for(int j = 0; j < alphabets.length; j++){
                if(password.charAt(i) == alphabets[j]){
                    newPassword += shiftAlphabets[j];
                }
            }
        }
        return  newPassword;
    }
    
