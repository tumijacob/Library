    private void btRunStatementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRunStatementActionPerformed
        String query = txtQuery.getText();
        DisplayTableResults table  = new DisplayTableResults(query);
        if(table.isQueryValid){
            table.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, table.errorMessage, 
                    "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btRunStatementActionPerformed

