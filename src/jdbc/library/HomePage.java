    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        String name = txtname.getText();
        String query = "SELECT FirstName, LastName, Title, EditionNumber FROM Authors "
                + "INNER JOIN AuthorISBN ON  Authors.AuthorID = "
                + "AuthorISBN.AuthorID INNER JOIN Titles ON AuthorISBN.ISBN = "
                + "Titles.ISBN WHERE lastName LIKE '%"+ name + "%' OR "
                + "FirstName LIKE '%" + name +"%'";
        new DisplayTableResults(query).setVisible(true);
    }//GEN-LAST:event_btSearchActionPerformed

