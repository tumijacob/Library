    public class MouseEventModel extends MouseAdapter{
        
        public void mouseClicked(MouseEvent e){
           
           String fName, lName, email, phoneNumber,username, password, userLevel;
            if(e.getClickCount() == 2){
                authorQueries = new AuthorsQueries();
                List<String> userList;
                int selectedRow =   jTable1.getSelectedRow();
                int authorId = Integer.parseInt(tableModel.getValueAt(
                        selectedRow, 0).toString());
                //select * from users where user is the clicked one
                 userList =   authorQueries.allUsers(authorId);
                
                //initializes user's variables with the data from database 
                fName = userList.get(0);
                lName = userList.get(1);
                email = userList.get(2);
                phoneNumber = userList.get(3);
                username = userList.get(4);
                password = userList.get(5);
                userLevel = userList.get(6);
                
                //register form with filled details to edit
                RegisterUser user =  new RegisterUser(authorId, fName,lName, email, 
                        phoneNumber, username, password, userLevel);
                user.setVisible(true);
                
            }
        }
    }
    
    
